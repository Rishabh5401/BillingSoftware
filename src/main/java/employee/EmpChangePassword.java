    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String old_password=jTextField1.getText();
        String new_password=jTextField2.getText();
        String cnew_password=jTextField3.getText();
        
        boolean status=dbOperation.checkOldPassword(old_password,rm.getEmail());
        if (status) {
    // âœ… Check if new password field is empty
            if (new_password.trim().isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "New Password cannot be empty", "Error", JOptionPane.ERROR_MESSAGE);
            } else if (!new_password.equals(cnew_password)) {
                JOptionPane.showMessageDialog(rootPane, "New Passwords didn't match", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                boolean status2 = dbOperation.updatePassword(new_password, rm.getEmail());
                if (status2) {
                    JOptionPane.showMessageDialog(rootPane, "Password Updated Successfully");
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField3.setText("");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Password not updated due to error", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Old Password didn't match", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    

